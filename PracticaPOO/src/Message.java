
enum Message {
    TITLE("--- CONECT4 ---"),
    HORIZONTAL_LINE("---------------------------------------------------------"),
    CONTINUE("Do you want to continue");

    private String message;

    Message(String message) {
        this.message = message;
    }

    void write() {
        Console.getInstance().write(this.message);
    }

    void writeln() {
        Console.getInstance().writeln(this.message);
    }

}